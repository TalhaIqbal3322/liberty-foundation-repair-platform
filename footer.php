<script type="text/javascript">

    $(document).ready(function () {
      $('#submit_postal_code').on('click', function () {

        $("#submit_postal_code").attr("disabled", "disabled");

        var postal_code_enter = $('#postal_code_enter').val().trim();

        if (postal_code_enter !== "") {


            $.ajax({
              url: "quotation-submit.php",
              type: "POST",
              data: {
                postal_code_enter: postal_code_enter
            },
            cache: false,
            success: function (response) {

              var dataResult = JSON.parse(response);


              if (dataResult.statusCode == 200) {
                Swal.fire({
                  icon: "success",
                  title: "Success",
                  text: "Request has been sent successfully. We will contact you soon"
              }).then((result) => {
                  if (result.isConfirmed) {
                    window.location.href = '';
                }
            });

          } else {
            showError(dataResult.message || "An unexpected error occurred.");
        }
    }
});




        }


        else {
          showError('Please fill all fields.');
      }



  });



      function showError(message) {
          Swal.fire({
            icon: "error",
            title: "Error",
            text: message
        });
          $("#submit_postal_code").removeAttr("disabled");
      }

  });
</script>

