        $(document).ready(function () {
          $('#submit_btn').on('click', function () {

            $("#submit_btn").attr("disabled", "disabled");

            var looking = $('#looking').val().trim();
            var postal_code = $('#postal_code').val().trim();
            var datetime = $('#datetime').val().trim();
            var name = $('#name').val().trim();
            var email = $('#email').val().trim();
            var phone_no = $('#phone_no').val().trim();
            var address = $('#address').val().trim();
            var comment = $('#comment').val().trim();

            var emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            var phonePattern = /^\d{10}$/;

            if (looking !== "" && postal_code !== "" && datetime !== "" && name !== "" && email !== "" && phone_no !== "" && address !== "" && comment !== "") {


                 $.ajax({
              url: "contact-submit.php",
              type: "POST",
              data: {
                looking: looking,
                postal_code: postal_code,
                datetime: datetime,
                name: name,
                email: email,
                phone_no: phone_no,
                address: address,
                comment: comment
              },
              cache: false,
              success: function (response) {

                  var dataResult = JSON.parse(response);

       
            

                  if (dataResult.statusCode == 200) {
                    Swal.fire({
                      icon: "success",
                      title: "Success",
                      text: "Request has been sent successfully. We will contact you soon"
                    }).then((result) => {
                      if (result.isConfirmed) {
                        window.location.href = '';
                      }
                    });

                  } else {
                    showError(dataResult.message || "An unexpected error occurred.");
                  }
              }
              });




              }
              

              else {
                  showError('Please fill all fields.');
              }



          });



          function showError(message) {
              Swal.fire({
                icon: "error",
                title: "Error",
                text: message
            });
              $("#submit_btn").removeAttr("disabled");
          }

      });
  </script>